# and operatoru
print(1 < 2 and 4 < 9)
print("mehmet" == "murat")
print(2 != 2)

# or operatoru
print(3 < 4 or 6 > 9)
print(3 > 4 or "mehmet" == "k1r")
print(3 > 4 or "mehmet" == "k1r" or 3.14 < 4.3)

# not operatoru
print(not 2 == 2)
print(not 2 != 2)
print(not (2.14 > 3.49 or (2 != 2 and "Murat" == "Murat")))
